// Write a C++ program to calculate the sum of two numbers.
#include<iostream>
using namespace std;
int main()
{
    int a,b, sum=0;
    cout<<"Enter value of a";
    cin>>a;
    cout<<"Enter value of b";
    cin>>b;
    sum = a + b;
    cout<<"The sum of two numbers is: "<<sum;
    return 0;
}